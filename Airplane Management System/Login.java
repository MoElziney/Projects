    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        
         email = EmailTextField.getText();
          password = new String(PasswordField.getPassword());
        
        if(AdminButton.isSelected()){
            Admin admin = new Admin();
//            System.out.print(dA.getAdminsData());
            admin=admin.verifyAccount(email, password,dA.getAdminsData());
            if(admin!=null){
            
            adminDashboardPage dashboard = new adminDashboardPage();
            dashboard.setAd(admin);
            dashboard.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Your account is not exist", "Not Found", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        
        else if(PassengerButton.isSelected()){
            Passenger passenger = new Passenger();
            passenger=passenger.verifyAccount(email, password,dA.getPassengersData());
            if(passenger!=null){
        
             PassengerDashboard dashboard = new PassengerDashboard();
             dashboard.setUser(passenger);
            dashboard.setVisible(true);
            }else{
               JOptionPane.showMessageDialog(null, "Your account is not exist", "Not Found", JOptionPane.INFORMATION_MESSAGE);
            }
        }
//        nextFrame.setVisible(true);
        
    }//GEN-LAST:event_LoginButtonActionPerformed

